package com.dev.ATSapp.Enums;

public enum CandidateStatus {
    APPLIED,        // Candidate applied for the job
    SHORTLISTED,    // Candidate is shortlisted for an interview
    IN_PROGRESS,    // Candidate is undergoing interview rounds
    SELECTED,       // Candidate passed all rounds
    REJECTED,       // Candidate is rejected
    OFFERED,        // Candidate received a job offer
    HIRED,          // Candidate accepted the offer and joined
    ONBOARDED,       // Candidate has completed onboarding
    ON_HOLD
}